		end)
	end)

	-- 10. Lobby.listGames()
	describe("listGames", function()
		it("returns empty array for no games", function()
			local lobby = Lobby.create()
			local games = Lobby.listGames(lobby)
			assert.is_table(games)
			assert.are.equal(0, #games)
		end)

		it("returns array of game info objects", function()

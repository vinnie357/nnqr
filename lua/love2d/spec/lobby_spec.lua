	-- 11. Lobby.startGame()
	describe("startGame", function()
		it("starts game, changes status to playing", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			-- Manually add second player without triggering auto-start
			-- Actually joinGame auto-starts, so let's use startGame differently
			-- We need a game with 2 players in waiting state
			-- For this test, we'll manually manipulate the game
			local game = lobby.games[gameId]
			-- Add p2 manually to test startGame
			table.insert(game.players, "p2")
			lobby.players["p2"].gameId = gameId
			-- Now manually keep status as waiting (it would be auto-set)
			game.status = "waiting"
			local success, err = Lobby.startGame(lobby, gameId)
			assert.is_true(success)
			assert.is_nil(err)
			assert.are.equal("playing", game.status)
		end)

		it("returns error if game not found", function()
			local lobby = Lobby.create()
			local success, err = Lobby.startGame(lobby, "nonexistent")
			assert.is_false(success)
			assert.are.equal("Game not found", err)
		end)

		it("returns error if not in waiting state", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			Lobby.joinGame(lobby, "p2", gameId) -- Auto-starts
			local success, err = Lobby.startGame(lobby, gameId)
			assert.is_false(success)
			assert.are.equal("Game not in waiting state", err)
		end)

		it("returns error if not enough players", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			local success, err = Lobby.startGame(lobby, gameId)
			assert.is_false(success)
			assert.are.equal("Not enough players", err)
		end)
	end)

	-- 12. Lobby.endGame()
	describe("endGame", function()
		it("ends game, sets status to finished", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			Lobby.joinGame(lobby, "p2", gameId)
			local success = Lobby.endGame(lobby, gameId, "p1")
			assert.is_true(success)
			local game = lobby.games[gameId]
			assert.are.equal("finished", game.status)
		end)

		it("stores winner ID", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			Lobby.joinGame(lobby, "p2", gameId)
			Lobby.endGame(lobby, gameId, "p1")
			local game = lobby.games[gameId]
			assert.are.equal("p1", game.winnerId)
		end)

		it("clears all players' gameId", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			Lobby.joinGame(lobby, "p2", gameId)

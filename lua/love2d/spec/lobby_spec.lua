		end)

		it("excludes players in games", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			Lobby.createGame(lobby, "p1", "Test Game")
			local players = Lobby.getAvailablePlayers(lobby)
			assert.are.equal(1, #players)
			assert.are.equal("Bob", players[1].name)
		end)
	end)

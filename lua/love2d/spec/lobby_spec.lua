			local lobby = Lobby.create()
			local success = Lobby.endGame(lobby, "nonexistent", nil)
			assert.is_false(success)
		end)
	end)

	-- 13. Lobby.cleanupFinishedGames()
	describe("cleanupFinishedGames", function()
		it("removes finished games from lobby", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")

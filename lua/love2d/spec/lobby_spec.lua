			assert.is_nil(lobby.games[gameId])
		end)

		it("returns count of removed games", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			Lobby.addPlayer(lobby, "p3", "Charlie")
			Lobby.addPlayer(lobby, "p4", "Diana")
			-- Create and finish two games
			local gameId1 = Lobby.createGame(lobby, "p1", "Game 1")
			Lobby.joinGame(lobby, "p2", gameId1)
			Lobby.endGame(lobby, gameId1, "p1")
			local gameId2 = Lobby.createGame(lobby, "p3", "Game 2")
			Lobby.joinGame(lobby, "p4", gameId2)
			Lobby.endGame(lobby, gameId2, "p3")
			local removed = Lobby.cleanupFinishedGames(lobby)
			assert.are.equal(2, removed)
		end)

		it("leaves non-finished games intact", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			Lobby.addPlayer(lobby, "p3", "Charlie")
			-- Create one waiting game and one finished game
			local waitingGameId = Lobby.createGame(lobby, "p1", "Waiting Game")
			local finishedGameId = Lobby.createGame(lobby, "p2", "Finished Game")
			Lobby.joinGame(lobby, "p3", finishedGameId)
			Lobby.endGame(lobby, finishedGameId, "p2")
			local removed = Lobby.cleanupFinishedGames(lobby)
			assert.are.equal(1, removed)
			assert.is_not_nil(lobby.games[waitingGameId])
			assert.is_nil(lobby.games[finishedGameId])
		end)
	end)

	-- 14. Lobby.getAvailablePlayerCount() - Phase 1: Player Count Tracking
	describe("getAvailablePlayerCount", function()
		it("returns 0 for empty lobby", function()
			local lobby = Lobby.create()
			assert.are.equal(0, Lobby.getAvailablePlayerCount(lobby))
		end)

		it("returns all players when no games", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			Lobby.addPlayer(lobby, "p3", "Charlie")
			assert.are.equal(3, Lobby.getAvailablePlayerCount(lobby))
		end)

		it("excludes players in waiting games", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			Lobby.addPlayer(lobby, "p3", "Charlie")
			Lobby.createGame(lobby, "p1", "Test Game") -- p1 is in game
			assert.are.equal(2, Lobby.getAvailablePlayerCount(lobby))
		end)

		it("excludes players in playing games", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			Lobby.addPlayer(lobby, "p3", "Charlie")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			Lobby.joinGame(lobby, "p2", gameId) -- Game starts, p1 and p2 playing
			assert.are.equal(1, Lobby.getAvailablePlayerCount(lobby)) -- Only p3
		end)

		it("includes players who left a game", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			Lobby.createGame(lobby, "p1", "Test Game")
			assert.are.equal(1, Lobby.getAvailablePlayerCount(lobby)) -- p2 only
			Lobby.leaveGame(lobby, "p1")
			assert.are.equal(2, Lobby.getAvailablePlayerCount(lobby)) -- Both available
		end)

		it("includes players after game ends", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			local gameId = Lobby.createGame(lobby, "p1", "Test Game")
			Lobby.joinGame(lobby, "p2", gameId)
			assert.are.equal(0, Lobby.getAvailablePlayerCount(lobby)) -- Both in game
			Lobby.endGame(lobby, gameId, "p1")
			assert.are.equal(2, Lobby.getAvailablePlayerCount(lobby)) -- Both available
		end)

		it("updates when player is removed from lobby", function()
			local lobby = Lobby.create()
			Lobby.addPlayer(lobby, "p1", "Alice")
			Lobby.addPlayer(lobby, "p2", "Bob")
			assert.are.equal(2, Lobby.getAvailablePlayerCount(lobby))
			Lobby.removePlayer(lobby, "p1")
			assert.are.equal(1, Lobby.getAvailablePlayerCount(lobby))
		end)
	end)

	-- 15. Lobby.getAvailablePlayers() - Returns list of available player info
	describe("getAvailablePlayers", function()
		it("returns empty table for empty lobby", function()
			local lobby = Lobby.create()
